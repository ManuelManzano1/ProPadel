package com.modelo;

public class Imagen {
	private int idPista;
	private String imagen;
	public Imagen() {
		super();
	}
	
	public int getIdPista() {
		return idPista;
	}

	public void setIdPista(int idPista) {
		this.idPista = idPista;
	}

	public String getImagen() {
		return imagen;
	}
	public void setImagen(String imagen) {
		this.imagen = imagen;
	}
	
	
}
