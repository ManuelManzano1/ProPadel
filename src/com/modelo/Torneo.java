package com.modelo;

public class Torneo {
	private int idTorneo;
	private String nombre;
	private int idPista;
	private String nombrePista;
	private String localizacion;
	private int numJugadores;
	private int numInscritos;
	private String infoPremios;
	private int inscrito;
	private String fecha;
	private int inscripcion;
	public int getIdTorneo() {
		return idTorneo;
	}
	public void setIdTorneo(int idTorneo) {
		this.idTorneo = idTorneo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getIdPista() {
		return idPista;
	}
	public void setIdPista(int idPista) {
		this.idPista = idPista;
	}
	
	public String getNombrePista() {
		return nombrePista;
	}
	public void setNombrePista(String nombrePista) {
		this.nombrePista = nombrePista;
	}
	public String getLocalizacion() {
		return localizacion;
	}
	public void setLocalizacion(String localizacion) {
		this.localizacion = localizacion;
	}
	public int getNumJugadores() {
		return numJugadores;
	}
	public void setNumJugadores(int numJugadores) {
		this.numJugadores = numJugadores;
	}
	public int getNumInscritos() {
		return numInscritos;
	}
	public void setNumInscritos(int numInscritos) {
		this.numInscritos = numInscritos;
	}
	public String getInfoPremios() {
		return infoPremios;
	}
	public void setInfoPremios(String infoPremios) {
		this.infoPremios = infoPremios;
	}
	public int getInscrito() {
		return inscrito;
	}
	public void setInscrito(int inscrito) {
		this.inscrito = inscrito;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public int getInscripcion() {
		return inscripcion;
	}
	public void setInscripcion(int inscripcion) {
		this.inscripcion = inscripcion;
	}
	
	
}
