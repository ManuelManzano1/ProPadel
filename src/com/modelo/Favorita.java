package com.modelo;

public class Favorita {
	private String usuario;
	private int idPista;
	
	public Favorita() {
		
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public int getIdPista() {
		return idPista;
	}

	public void setIdPista(int idPista) {
		this.idPista = idPista;
	}

	
	
	
}
